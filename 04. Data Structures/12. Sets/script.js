// ANCHOR: Sets
/* NOTE: 
A JavaScript Set is a built-in object that stores a collection of unique values of any type. Unlike arrays, a Set cannot hold duplicate values; any duplicate addition is automatically ignored.
*/
const orderSet = new Set([
  'Pasta',
  'Pizza',
  'Pizza',
  'Risotto',
  'Pasta',
  'Pizza',
]);
console.log(orderSet);

